    CharCount[] getLetterFrequency(String text) {
        CharCount letterCount[] = new CharCount[26];
         
        text = text.toUpperCase();
        
        for(int j = 0; j < 26; j++) {
            letterCount[j] = new CharCount((char)(65 + j));
        }
        
        for(int i = 0; i < text.length(); i++) {
            char curChar = text.charAt(i);
            //curChar = Character.toUpperCase(curChar);
            int value = (int)curChar;
            value = value - 65;
            if(value >= 0 && value <= 25) {
                letterCount[value].increment();
            }
        }
        
        return letterCount;
        
    }
    
    CharCount[] sortCharCount(CharCount[] input) {
        Arrays.sort(input);
        return input;
    }
    
    private void highlight(char curChar) {
      Highlighter h = DecryptedText.getHighlighter();
      h.removeAllHighlights();
      String text = DecryptedText.getText().toUpperCase();

      Highlighter.HighlightPainter yellowPainter = new DefaultHighlighter.DefaultHighlightPainter(Color.yellow);
      
      for (int j = 0; j < text.length(); j += 1) {
        char ch = text.charAt(j);
        if (ch == curChar) {
            try {
                h.addHighlight(j, j + 1, yellowPainter/*DefaultHighlighter.DefaultPainter*/);
                
            } catch (BadLocationException e) {
            }
        }
      }
    }
